package Maps;

public class Map_intro {
	
	
	
	/**-------------------------- Map interface -----------------------------------
	 * Maps Interface used to store the elements in pair, as key value pair structure.
	 * Based on Key + Value pairs.
	 * Maps interface does not extend Collection Interface.
	 * Keys in the map are always uniques, cannot be repeated. Values can be duplicate.
	 * 
	 *    MAP > HashMap     - Implements to Map interface,Non thread safe, allows 1 null key.
	 *        > HashTable   - Thread safe, does not allow null key and duplicate keys.
	 *        > SortedMap 
	 *                 > TreeMap. - keys are sorted by ascending order
	 */

}
